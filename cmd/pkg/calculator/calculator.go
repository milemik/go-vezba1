package calculator

// SumNums - return the sum of two values
func SumNums(x, y float64) float64 {
	return x + y
}

// SubtractNums - will substract first number from second
func SubtractNums(x, y float64) float64 {
	return x - y
}

// MultipleNums - will multiply first number with second
func MultipleNums(x, y float64) float64 {
	return x * y
}

// DivideNums will divide first number with second
func DivideNums(x, y float64) float64 {
	return x / y
}

// Module will return module of two numbers
func ModuleNums(x, y float64) int {
	return int(x) % int(y)
}
